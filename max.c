int		max(int *tab, unsigned int len)
{
	unsigned int	i;
	int		temp;
	i = 0;

	if (len == 1)
		return (tab[i]);
	while (i <= (len - 1))
	{
		if (tab[i] < tab[i + 1])
			temp = tab[i];
		i++;
	}
	return (temp);
}
