char    *ft_strrev(char *str)
{
    int     i = 0;
    int     j = 0;
    char    temp;
    while (str[j] != '\0')
        j++;
    while (i < j)
    {
        temp = str[i];
        str[i] = str[j - 1];
        str[j - 1] = temp;
        i++;
        j--;
    }
    return (str);
}